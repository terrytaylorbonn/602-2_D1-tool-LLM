def add(a, b):
    return a + b


def subtract(a, b):
    return a - b


def divide(a, b):
    return a / b


def main():
    print("add(2,3) =", add(2,3))
    print("divide(10,2) =", divide(10,2))
    print("subtract(5,2) =", subtract(5,2))


if __name__ == "__main__":
    main()
